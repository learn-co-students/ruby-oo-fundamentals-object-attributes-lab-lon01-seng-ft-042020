#Define a class Person
class Person

    #Define the setter method for Person name
    #Into an instance variable @name
    def name=(person_name)
        @name = person_name
    end
    
    #Read the name of a Person instance via instance variable @name
    def name
        @name
    end
    
    
    #Define the setter method for Person job
    #Into an instance variable @job
    def job=(person_job)
        @job = person_job
    end
    
    #Read the job of a Person instance via instance variable @job
    def job
        @job
    end
    
    end