#Define a class Dog
class Dog

#Define the setter method for Dog name
#Into an instance variable @name
def name=(dog_name)
    @name = dog_name
end

#Read the name of a Dog instance via instance variable @name
def name
    @name
end


#Define the setter method for Dog breed
#Into an instance variable @breed
def breed=(dog_breed)
    @breed = dog_breed
end

#Read the breed of a Dog instance via instance variable @breed
def breed
    @breed
end

end
